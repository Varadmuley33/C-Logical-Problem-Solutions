#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   Prints pattern: 1 * 2 * 3 * 4 * 5 *
//
/////////////////////////////////////////////////////////////////

void Display()
{
    printf("1\t*\t");
    printf("2\t*\t");
    printf("3\t*\t");
    printf("4\t*\t");
    printf("5\t*\t");              // Pattern output
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                      // Function call

    return 0;
}   // End of main

