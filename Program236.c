#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Change
//  Description :   Replaces lowercase alphabetic characters with
//                  '_' in the string.
//  Input :         Character Array
//  Output :        Void
//  Author :        Varad Nitin Muley
//
/////////////////////////////////////////////////////////////////

void Change(char str[])
{
    while(*str != '\0')
    {
        if((*str >= 'a') && (*str <= 'z'))
        {
            *str = '_';
        }

        str++;
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main ()
{
    char Arr[50] = {'\0'};

    printf("Enter string : \n");
    scanf("%[^'\n]s",Arr);

    Change(Arr);

    printf("Updated string is : %s\n" ,Arr);

    return 0;
}

