#include <stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Returns the addition of two integer values
//
/////////////////////////////////////////////////////////////////

int Addition(int No1, int No2)
{
    int Ans = 0;

    Ans = No1 + No2;

    return Ans;
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    printf("%d\n", Addition(10, 11));

    return 0;
}

