#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : CountSpace
//  Description :   Counts space characters from the given string.
//  Input :         Character Array
//  Output :        Void
//  Author :        Varad Nitin Muley
//
/////////////////////////////////////////////////////////////////

void CountSpace(char str[])
{
    int iCount = 0;

    while(*str != '\0')
    {
        if(*str == ' ')
        {
            iCount++;
        }

        str++;
    }

    printf("Number of Spaces  : %d\n" ,iCount);
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main ()
{
    char Arr[50] = {'\0'};

    printf("Enter string : \n");
    scanf("%[^'\n]s",Arr);

    CountSpace(Arr);

    return 0;
}

