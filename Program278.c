/////////////////////////////////////////////////////////////////
//
//  Required Header files
//
/////////////////////////////////////////////////////////////////

#include <stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Structure to store two integer values
//
/////////////////////////////////////////////////////////////////

struct Arithematic
{
    int No1;
    int No2;
};

/////////////////////////////////////////////////////////////////
//
//  Initializes the structure members with given values
//
/////////////////////////////////////////////////////////////////

void Initialize(struct Arithematic *ptr, int A, int B)
{
    ptr->No1 = A;
    ptr->No2 = B;
}

/////////////////////////////////////////////////////////////////
//
//  Returns the addition of two structure members
//
/////////////////////////////////////////////////////////////////

int Addition(struct Arithematic *ptr)
{
    int Ans = 0;

    Ans = ptr->No1 + ptr->No2;

    return Ans;
}

/////////////////////////////////////////////////////////////////
//
//  Returns the subtraction of two structure members
//
/////////////////////////////////////////////////////////////////

int Substraction(struct Arithematic *ptr)
{
    int Ans = 0;

    Ans = ptr->No1 - ptr->No2;

    return Ans;
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    struct Arithematic aobj1, aobj2;

    Initialize(&aobj1, 11, 10);
    Initialize(&aobj2, 101, 50);

    printf("%d\n", Addition(&aobj1));
    printf("%d\n", Substraction(&aobj1));

    printf("%d\n", Addition(&aobj2));
    printf("%d\n", Substraction(&aobj2));

    return 0;
}

