#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints lowercase alphabets pattern
//  Input         : Integer
//  Output        : None
//  Author        : Varad Nitin Muley
//  Date          : 18/11/2025
//
/////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0;
    char ch = 'a';
 
    for (iCnt = 1 ; iCnt <= iNo ; iCnt++)
    { 
        printf("%c\t", ch);
        ch++;
    }
    printf("\n"); 
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function
//
/////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0 ;

    printf("Enter the Frequency: \n");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

