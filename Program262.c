#include <stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Returns the addition of two numbers
//
/////////////////////////////////////////////////////////////////

int Add(int No1, int No2)
{
    int Ans;

    Ans = No1 + No2;

    return Ans;
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 10, iValue2 = 11;
    int iRet = 0;

    iRet = Add(iValue1, iValue2);

    printf("%d\n", iRet);

    return 0;
}

