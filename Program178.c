#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints reverse lowercase alphabets pattern
//  Input         : Integer
//  Output        : None
//  Author        : Varad Nitin Muley
//  Date          : 18/11/2025
//
/////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0;
    char ch = 'z';
 
    for (iCnt = 1 ; iCnt <= iNo ; iCnt++, ch--)
    { 
        printf("%c\t", ch);
    }
    printf("\n"); 
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function
//
/////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0 ;

    printf("Enter the Frequency: \n");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

