#include <stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function name : Display
//  Description   : Attempts to display numbers from -iNo to 0
//  Input         : Integer
//  Output        : Void
//  Author        : Varad Nitin Muley
//  Date          : 18/11/2025
//
/////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0 ;

    for(iCnt = -iNo ; iCnt <= 0 ; iCnt++);
    {
        printf("%d\t",iCnt);
    }

    printf("\n");
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function
//
/////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0 ;

    printf("Enter the Frequency : \n");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

