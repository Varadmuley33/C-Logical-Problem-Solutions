#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints pattern using numbers and *
//  Input         : Integer
//  Output        : None
//  Author        : Varad Nitin Muley
//  Date          : 18/11/2025
//
/////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0 , iCount = 0;

    iCount = 1;
    for(iCnt = 1 ; iCnt <= iNo ; iCnt++)
    {
        if((iCnt % 2) == 0)
        {
            printf("*\t");
        }
        else
        {
            printf("%d\t", iCount);
            iCount++;
        }
    }

    printf("\n");
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function
//
/////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0 ;

    printf("Enter the Frequency : \n");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

