#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Display function
//
/////////////////////////////////////////////////////////////////

void Display()
{
    auto int iCnt = 0;
    printf("Jay Ganesh ...%d\n", iCnt);
    iCnt++;
    Display();
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

