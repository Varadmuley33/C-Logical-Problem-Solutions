#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   Prints numbers 5 to 1 using a decreasing for loop
//
/////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 0;                          // Loop counter

    for(iCnt = 5; iCnt >= 1; iCnt--)       // Reverse loop
    {
        printf("%d\t", iCnt);              // Print number
    }

    printf("\n");                           // New line
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                              // Function call

    return 0;
}   // End of main

