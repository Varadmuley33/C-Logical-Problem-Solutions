/////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  auto_demo function
//
/////////////////////////////////////////////////////////////////

void auto_demo()
{
    auto int i = 1;
    printf("auto_demo : %d\n", i);
    i++;
}

/////////////////////////////////////////////////////////////////
//
//  static_demo function
//
/////////////////////////////////////////////////////////////////

void static_demo()
{
    static int i = 1;
    printf("static_demo : %d\n", i);
    i++;
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function
//
/////////////////////////////////////////////////////////////////

int main()
{
    auto_demo();
    auto_demo();
    auto_demo();
    auto_demo();

    static_demo();
    static_demo();
    static_demo();
    static_demo();
    
    return 0;
}

