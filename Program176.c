#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints uppercase alphabets pattern
//  Input         : Integer
//  Output        : None
//  Author        : Varad Nitin Muley
//  Date          : 18/11/2025
//
/////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0;
    char ch = 'A';
 
    for (iCnt = 1 ; iCnt <= iNo ; iCnt++,ch++)
    { 
        printf("%c\n", ch);
    }
    printf("\n"); 
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function
//
/////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0 ;

    printf("Enter the Frequency: \n");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

