#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   Prints "Jay Ganesh..." four times using a loop
//
/////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 0;                      // Loop counter

    for(iCnt = 1; iCnt <= 4; iCnt++)
    {
        printf("Jay Ganesh...\n");     // Repeated message
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    // Calling Display function (similar to method call in Java)
    Display();

    return 0;
}   // End of main

