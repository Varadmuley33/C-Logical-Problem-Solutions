#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Structure Name : Number
//  Description    : Stores the loop counter used for factor calculation.
//
/////////////////////////////////////////////////////////////////

struct Number
{
    int i;
};

/////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayFactors
//  Description   : Displays all the factors of the given number.
//  Input         : Structure Pointer, Integer
//  Output        : Factors of the given number
//
/////////////////////////////////////////////////////////////////

void DisplayFactors(struct Number *this, int iNo)
{
    for(this->i = 1; this->i <= (iNo/2); this->i++)
    {
        if((iNo % this->i) == 0)
        {
            printf("%d\n", this->i);
        }
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : ");
    scanf("%d", &iValue);

    struct Number nobj;

    DisplayFactors(&nobj, iValue);

    return 0;
}

