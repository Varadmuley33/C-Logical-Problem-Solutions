#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   Prints numbers 5 4 3 2 1 using tab spacing
//
/////////////////////////////////////////////////////////////////

void Display()
{
    printf("5\t");
    printf("4\t");
    printf("3\t");
    printf("2\t");
    printf("1\t");
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                             // Function call

    return 0;
}   // End of main

